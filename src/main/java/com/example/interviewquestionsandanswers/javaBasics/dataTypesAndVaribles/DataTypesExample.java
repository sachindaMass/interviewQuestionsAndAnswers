package com.example.interviewquestionsandanswers.javaBasics.dataTypesAndVaribles;


public class DataTypesExample {

    public DataTypesExample() {
    }

    //Write a Java program to declare and initialize a variable of type int with the value 42.
    public void intDataType() {
        int num = 42;
        System.out.println(42);
    }

    //    Write a Java program to declare and initialize a variable of type double with the value 3.14.
    public void doubleDataType() {
        double num = 3.14;
        System.out.println(num);

    }

    //    Write a Java program to declare and initialize a variable of type boolean with the value true.
    public void booleanDataType() {
        boolean flag = true;
        System.out.println(flag);
    }

    //    Write a Java program to declare and initialize a variable of type String with the value "Hello, world!".
    public void stringDataType() {
        String message = "Hello world";
        System.out.println(message);
    }
//    Write a Java program to declare and initialize a variable of type char with the value 'A'.

    public void charDataType() {
        char letter = 'A';
        System.out.println(letter);
    }
}
